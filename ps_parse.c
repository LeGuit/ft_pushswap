int			parse_opt(int ac, char **av, int *opt)
{
	int		i;

	UNSET(*opt, OPT_ALL);
	if (av[1][0] != '-' && (av[1][1] == 'c' || av[1][1] == 'v'))
		return (0);
	i = 1;
	while (i < ft_strlen(av[1]))
	{
		if (!ft_strchr("cv", av[1][i]))
			ft_exit();
	}
}
